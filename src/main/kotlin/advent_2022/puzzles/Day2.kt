    SCISSOR_2 to mapOf(
        NEED_LOOSE to LOSS_POINT + PAPER_POINT,
        NEED_DRAW to DRAW_POINT + SCISSOR_POINT,
        NEED_WIN to WIN_POINT + ROCK_POINT
    ),
)

