private const val ROCK_1 = 'X'
private const val PAPER_1 = 'Y'
private const val SCISSOR_1 = 'Z'

private const val ROCK_2 = 'A'
private const val PAPER_2 = 'B'
private const val SCISSOR_2 = 'C'

private const val NEED_LOOSE = 'X'
private const val NEED_DRAW = 'Y'
private const val NEED_WIN = 'Z'

private const val ROCK_POINT = 1
private const val PAPER_POINT = 2
private const val SCISSOR_POINT = 3

private const val LOSS_POINT = 0
private const val DRAW_POINT = 3
private const val WIN_POINT = 6

fun day2() {
    val strategy1 = input.reader()
        .readLines()
        .sumOf { strategy1[it[2]]?.get(it[0]) ?: 0 }

    val strategy2 = input.reader()
        .readLines()
        .sumOf { strategy2[it[0]]?.get(it[2]) ?: 0 }

    println("December 2nd (Part 1): $strategy1")
    println("December 2nd (Part 2): $strategy2")
}

private val strategy1 = mapOf(
    ROCK_1 to mapOf(
        ROCK_2 to DRAW_POINT + ROCK_POINT,
        PAPER_2 to LOSS_POINT + ROCK_POINT,
        SCISSOR_2 to WIN_POINT + ROCK_POINT
    ),
    PAPER_1 to mapOf(
        ROCK_2 to WIN_POINT + PAPER_POINT,
        PAPER_2 to DRAW_POINT + PAPER_POINT,
        SCISSOR_2 to LOSS_POINT + PAPER_POINT
    ),
    SCISSOR_1 to mapOf(
        ROCK_2 to LOSS_POINT + SCISSOR_POINT,
        PAPER_2 to WIN_POINT + SCISSOR_POINT,
        SCISSOR_2 to DRAW_POINT + SCISSOR_POINT
    ),
)

private val strategy2 = mapOf(
    ROCK_2 to mapOf(
        NEED_LOOSE to LOSS_POINT + SCISSOR_POINT,
        NEED_DRAW to DRAW_POINT + ROCK_POINT,
        NEED_WIN to WIN_POINT + PAPER_POINT
    ),
    PAPER_2 to mapOf(
        NEED_LOOSE to LOSS_POINT + ROCK_POINT,
        NEED_DRAW to DRAW_POINT + PAPER_POINT,
        NEED_WIN to WIN_POINT + SCISSOR_POINT
